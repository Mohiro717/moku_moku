export async function getIntroStory(): Promise<string> {
  return `むかしむかし、あるところに、こころのやさしいちいさなおひめさまがいました。
けれどあるひ、いじわるなまじょにまほうをかけられてしまい、なんとちいさなカエルのすがたにかえられてしまったのです。

まじょはおひめさまのおしろをうばい、おしろのまわりにぐるりとおおきなめいきゅうをつくりました。
そしてカエルになったおひめさまを、そのめいきゅうのなかにとじこめてしまったのです。

でも、おひめさまはあきらめません。
もとのすがたにもどって、おしろをとりもどすために、ゆうきをふりしぼってぼうけんにでることにしました。

めいきゅうのふういんをとくには、「みっつのまほうのオーブ」をみつけなくてはいけません。
でもそのみちのりには、まじょのしかけたわなや、おそろしいまものたちがいっぱいまちかまえています。

それでも、ちいさなかえるのおひめさまはまけません。
ちいさなからだに、おおきなゆうきをつめこんで――
さあ、ふしぎでかわいいだいぼうけんのはじまりです！`;
}

